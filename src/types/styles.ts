/**
 * `enum` encapsulating tailwind font weight class tags
 *
 * @example
 * FontWeight.light => "light"
 * FontWeight.extrabold => "extrabold"
 */
export const enum FontWeight {
  thin = "thin",
  extralight = "extralight",
  light = "light",
  normal = "normal",
  medium = "medium",
  semibold = "semibold",
  bold = "bold",
  extrabold = "extrabold",
  black = "black",
}

